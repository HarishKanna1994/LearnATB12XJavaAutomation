package ex10_ForLoop;

public class Lab083_If_ForLoopmix {
    public static void main(String[] args) {
        for(int shruti=0;shruti<18;shruti++) //0 to 17 18 times
        {
            if(shruti > 15) {
                System.out.println("Gift from Papa");
            }
            else
            {
                System.out.println("No gift");
            }
        }
    }
}
