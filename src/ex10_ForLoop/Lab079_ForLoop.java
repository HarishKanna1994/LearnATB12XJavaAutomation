package ex10_ForLoop;

public class Lab079_ForLoop {
    public static void main(String[] args) {
        //For Loop
        //Helps you to repeat a block of code
        //Initialization, Condition and Updation(Increment/Decrement) -> ICU
        //ICU
        for(int i=0;i<10;i++)    //It has run 10 times...And it prints 0 to 9
        {
            System.out.println(i);
        }
    }
}
