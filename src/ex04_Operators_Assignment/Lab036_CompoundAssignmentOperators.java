package ex04_Operators_Assignment;

public class Lab036_CompoundAssignmentOperators {
    public static void main(String[] args) {
        int age = 10;
        age += 10;  //age = age + 10
        age -= 10;  //age = age - 10
        age *= 10;  //age = age * 10
        age /= 10;  //age = age/10
        System.out.println(age);

        //Bitwise operators  or Scientific Operators
        /* >> -> Right shift
         << -> Left Shift
         ~ -> Bitwise Not
         ^ -> XOR Operator
         */
    }
}
