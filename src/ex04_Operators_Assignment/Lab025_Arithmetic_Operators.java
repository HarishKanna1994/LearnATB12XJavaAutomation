package ex04_Operators_Assignment;

public class Lab025_Arithmetic_Operators {
    public static void main(String[] args) {
        //Arithmetic Operators
        /* + -> Addition
           - -> Subtraction
           * -> Multiplication
           / -> Division
           % -> Modulus}
         */
        int a = 20;
        int b = 3;
        float c = 3.0f;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a/c);
        System.out.println("a+b");
        System.out.println("a"+"b");
    }
}

