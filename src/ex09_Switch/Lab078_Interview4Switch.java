package ex09_Switch;

public class Lab078_Interview4Switch {
    public static void main(String[] args) {
        char code = 'C';
        switch(code)
        {
            default:
                System.out.println("Helloooooo");
                break;
            case 'A':
                System.out.println("A");
                break;
            case 'B':
                System.out.println("B");
                break;


        }
    }

}
