package ex09_Switch;

public class Lab077_InterviewSwitch {
    public static void main(String[] args) {
        int a = 11;
        switch(-1)
        {
            default:
                System.out.println("None");
                break;
            case -1:
                System.out.println("10");
                break;
            case 9:
                System.out.println("9");
                break;

        }
    }
}
